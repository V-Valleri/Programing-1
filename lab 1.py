"""
Задача №4.

Дано рядок. Замініть у цьому рядку усі входження певної літери англійського
алфавіту, яку вводить користувач у нижньому регістрі, на відповідну літеру
у верхньому регістрі. Спочатку вводиться літера, а потім рядок, у якому
треба виконати заміну.

Автор: Костючик Валерія Дмитрівна
"""

# Отримати від користувача літеру та рядок
letter = input("Введіть літеру для заміни: ").lower()
string = input("Введіть рядок для заміни: ")

# Перевірити, чи введено лише одну літеру
if len(letter) == 1 and letter.isalpha():
    # Виконати заміну у рядку
    mod_string = string.replace(letter, letter.upper())

    # Вивести результат
    print("Результат після заміни:", mod_string)
else:
    print("Будь ласка, введіть лише одну літеру.")

    
