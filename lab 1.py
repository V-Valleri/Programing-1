"""
Задача №1.

Тетяна кожен день лягає спати рівно опівночі і нещодавно дізналась,
що оптимальний час для її сну становить t хвилин. Тетяна хоче поставити
собі будильник так, щоб він продзвенів рівно через t хвилин після півночі,
однак для цього необхідно вказати час сигналу у форматі години і хвилини.
Допоможіть Тані визначити, на який час завести будильник. Години і хвилини у
виведенні програми повинні розташовуватися на різних рядках.

Автор: Костючик Валерія Дмитрівна
"""
# Отримати від користувача час сну у хвилинах
t = int(input("Введіть тривалість сну у хвилинах: "))

# Розрахувати години і хвилини для будильника
hours = t // 60
minutes = t % 60

# Вивести результат
print("Час для поставлення будильника:")
print("Години:", hours)
print("Хвилини:", minutes)

"""
Задача №2.

Потрібно підрахувати, на скільки раніше буде закінчуватися k-й урок,
якщо всі перерви скоротити на 5 хвилин. Вводиться одне натуральне число k,
не більше 7. Необхідно вивести одне натуральне число - час у хвилинах.

Автор: Костючик Валерія Дмитрівна
"""

# Отримати від користувача номер уроку k
k = int(input("Введіть номер уроку (не більше 7): "))

# Визначити тривалість уроків та перерв у хвилинах
t_ur = 45
t_per = [5, 10, 20, 10, 10, 5, 10]

# Перевірити, чи введений номер уроку в діапазоні від 1 до 7
if 1 <= k <= 7:
    # Розрахувати час тривалості уроків та перерв для перших k-1 уроків
    s_t_ur = (k - 1) * t_ur
    s_t_per = sum(t_per[:k - 1])

    # Загальний час у хвилинах
    t = s_t_ur + s_t_per

    # Скоротити перерви на 5 хвилин
    new_t = t - (5 * (k - 1))

    # Вивести результат
    print("Загальний час у хвилинах після скорочення перерв на 5 хвилин:", new_t)
else:
    print("Невірний номер уроку. Введіть число від 1 до 7.")

